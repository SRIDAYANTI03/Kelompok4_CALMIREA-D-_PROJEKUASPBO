private void tampilData() {
    try {
        // 1. Ambil data dari database terlebih dahulu
        String sql = "SELECT * FROM buku";
        ResultSet rs = stm.executeQuery(sql);

        // 2. Buat objek tabel baru dengan kepala kolom (8 Kolom Lengkap)
        String[] kepalaKolom = {"Id Buku", "Judul", "Penulis", "Kategori", "Tahun Terbit", "Stok", "Status", "Id Rak"};
        DefaultTableModel modelBaru = new DefaultTableModel(kepalaKolom, 0);

        // 3. Masukkan baris data dari database ke dalam model baru
        while (rs.next()) {
            modelBaru.addRow(new Object[]{
                rs.getString("id_buku"),
                rs.getString("judul"),
                rs.getString("penulis"),
                rs.getString("kategori"),
                rs.getString("tahun_terbit"),
                rs.getString("stok"),
                rs.getString("status"),   
                rs.getString("id_rak")    
            });
        }
        
        jTable1.setModel(modelBaru);
        
        // 4. Tampilkan jumlah total data
        jLabel2.setText("Total Data : " + modelBaru.getRowCount());

    } catch (SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(null, "Error Database: " + e.getMessage());
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(null, "Error Aplikasi: " + e.getMessage());
    }
}
        
        

