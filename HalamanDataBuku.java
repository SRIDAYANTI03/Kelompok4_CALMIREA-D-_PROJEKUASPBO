    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

    int baris = jTable1.getSelectedRow();
    
   if (baris != -1) {
        jTextField4.setText(jTable1.getValueAt(baris, 0).toString()); // ID Buku
        jTextField2.setText(jTable1.getValueAt(baris, 1).toString()); // Judul
        jTextField3.setText(jTable1.getValueAt(baris, 2).toString()); // Penulis
        jComboBox1.setSelectedItem(jTable1.getValueAt(baris, 3).toString()); // Kategori
        jTextField6.setText(jTable1.getValueAt(baris, 4).toString()); // Tahun Terbit
        jTextField5.setText(jTable1.getValueAt(baris, 5).toString()); // Stok
        jComboBox3.setSelectedItem(jTable1.getValueAt(baris, 6).toString()); // Status
        jComboBox2.setSelectedItem(jTable1.getValueAt(baris, 7).toString()); // ID Rak

        jTextField4.setEditable(false); // Kunci ID Buku saat edit
    }
    }//GEN-LAST:event_jTable1MouseClicked

