    private void simpanBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanBTActionPerformed

        // 1. Tampung dulu semua isian dari layar
        String idBuku = idbukuCMB.getSelectedItem().toString();
        String namaPeminjam = namapeminjamTF.getText(); 
        String judul = judulbukuTF.getText();
        String tglPinjam = tglpinjamTF.getText();
        String tglKembali = tglkembaliTF.getText();
        String status = "Dipinjam";
        
        // Kondisi buku dan denda dikosongkan saat pertama kali pinjam
        String kondisiBuku = "-"; 
        String denda = "0";

        try {
            java.sql.Connection conn = Koneksi.getKoneksi();
            
            // 2. Sesuaikan ejaan kolom dengan yang ada di gambar database Kakak
            // Perhatikan kolom ke-3 adalah "judul" (bukan "judul_buku")
            String sql = "INSERT INTO peminjaman (nama_peminjam, id_buku, tanggal_pinjam, tanggal_kembali, status, kondisi_buku, denda) "
           + "VALUES (?, ?, ?, ?, ?, ?, ?)";
            
           java.sql.PreparedStatement pst = conn.prepareStatement(sql);

             pst.setString(1, namaPeminjam); // dari textfield nama peminjam
             pst.setString(2, idBuku);
             pst.setString(3, tglPinjam);
             pst.setString(4, tglKembali);
             pst.setString(5, status);
             pst.setString(6, kondisiBuku);
             pst.setString(7, denda);

             pst.executeUpdate();
            
            javax.swing.JOptionPane.showMessageDialog(this, "Mantap! Data resmi masuk ke Database!");

            // 4. Update tabel di layar
            DefaultTableModel model = (DefaultTableModel) peminjamanTB.getModel();
            model.addRow(new Object[]{
                model.getRowCount() + 1,
                namaPeminjam,
                idBuku,
                judul,
                tglPinjam,
                tglKembali,
                status
            });

            totaldataTF.setText(String.valueOf(model.getRowCount()));

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Oops Gagal Simpan: " + e.getMessage());
        }
 
   
    }//GEN-LAST:event_simpanBTActionPerformed

