    private void idbukuCMBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idbukuCMBActionPerformed
                                                                  
    String idTerpilih = idbukuCMB.getSelectedItem() != null ? idbukuCMB.getSelectedItem().toString() : "";
    
    if (!idTerpilih.isEmpty() && !idTerpilih.equals("- Pilih ID Buku -") && !idTerpilih.equals("Item 1")) {
        try {
            // Menggunakan jalur koneksi database project kamu
            java.sql.Connection conn = Koneksi.getKoneksi(); 
            String sql = "SELECT judul, penulis, penerbit, stok FROM buku WHERE id_buku = ?";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, idTerpilih);
            java.sql.ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
                // Semua data buku langsung otomatis disuntikkan ke kolom teks masing-masing
                judulbukuTF.setText(rs.getString("judul"));
                penulisTF.setText(rs.getString("penulis"));
                kategoriTF.setText(rs.getString("penerbit")); // mengambil data dari kolom penerbit database
                stokTF.setText(rs.getString("stok"));
            }
        } catch (Exception e) {
            System.out.println("Gagal memuat detail buku: " + e.getMessage());
        }
    } else {
        // Kosongkan form jika default yang terpilih
        judulbukuTF.setText("");
        penulisTF.setText("");
        kategoriTF.setText("");
        stokTF.setText("");
    }

    }//GEN-LAST:event_idbukuCMBActionPerformed

    private void cariBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariBTActionPerformed
                                     
    // Mengambil ID Buku dari kolom inputan (Asumsi namanya sesuai komponen yang tertera di kode: idbukuCMB)
    String idTerpilih = idbukuCMB.getSelectedItem() != null ? idbukuCMB.getSelectedItem().toString() : "";
    
    if (idTerpilih.isEmpty() || idTerpilih.equals("- Pilih ID Buku -")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Silakan masukkan atau pilih ID Buku terlebih dahulu!");
        return;
    }

    try {
        java.sql.Connection conn = KoneksiDataUser.getKoneksi();
        String sql = "SELECT judul, penulis, penerbit, stok FROM buku WHERE id_buku = ?";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, idTerpilih);
        java.sql.ResultSet rs = pst.executeQuery();
        
        if (rs.next()) {
            // Berhasil Menemukan Buku, Teks Otomatis Terisi!
            judulbukuTF.setText(rs.getString("judul"));
            penulisTF.setText(rs.getString("penulis"));
            kategoriTF.setText(rs.getString("penerbit")); 
            stokTF.setText(rs.getString("stok"));
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "ID Buku tidak ditemukan di database!");
        }
    } catch (Exception e) {
        System.out.println("Gagal mengambil detail buku: " + e.getMessage());
    }
    }//GEN-LAST:event_cariBTActionPerformed

